#include<iostream>
#include<cstring>
using namespace std;
class A{
	private:
		friend class B;
		char *name;
		int age;
	public:
		A(char *str,int n){
			name = new char[strlen(str) + 1];
			strcpy(name,str);
			age = n;
		}
}; 
class B{
	public:
		void show(A x){
			cout << "姓名:" << x.name << endl;
			cout << "年龄:" << x.age << endl; 
		}
};
/*class B;声明格式 
class A{
	...
	friend class B;
	....//B成为A的友元类 
};*/
int main(){
	A a("左力",19);
	B b;
	b.show(a); //由于类A把类B声明为它的友元类，所以类B的成员函数相应的都成为了类A的友元函数，可以自由访问类A的私有数据成员name和age 
	return 0;
}
